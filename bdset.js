const navList = document.getElementById("navList");
const deck = document.getElementById("deck");
const progressBar = document.getElementById("progressBar");
const slideCounter = document.getElementById("slideCounter");
const prevBtn = document.getElementById("prevBtn");
const nextBtn = document.getElementById("nextBtn");

let currentSlide = 0;
let charts = {};

if (window.ChartDataLabels && window.Chart) {
  Chart.register(window.ChartDataLabels);
}

function buildDeck() {
  navList.innerHTML = slides
    .map((slide, index) => `<li><button data-index="${index}" class="${index === 0 ? "active" : ""}">${index + 1}. ${slide.title}</button></li>`)
    .join("");

  deck.innerHTML = slides
    .map(
      (slide, index) => `
        <article class="slide ${index === 0 ? "active" : ""}" data-slide="${index}">
          <header class="slide-head">
            <h2>${slide.title}</h2>
            <p>${slide.subtitle}</p>
          </header>
          <div class="slide-content">${slide.render()}</div>
        </article>
      `
    )
    .join("");

  navList.addEventListener("click", (event) => {
    const button = event.target.closest("button[data-index]");
    if (!button) return;
    goToSlide(Number(button.dataset.index));
  });

  prevBtn.addEventListener("click", () => goToSlide(currentSlide - 1));
  nextBtn.addEventListener("click", () => goToSlide(currentSlide + 1));

  document.addEventListener("keydown", (event) => {
    if (event.key === "ArrowRight" || event.key === "PageDown") goToSlide(currentSlide + 1);
    if (event.key === "ArrowLeft" || event.key === "PageUp") goToSlide(currentSlide - 1);
  });

  updateUi();
  runSlideHook();
}

function goToSlide(index) {
  if (index < 0 || index >= slides.length) return;
  currentSlide = index;

  [...deck.querySelectorAll(".slide")].forEach((slide, i) => slide.classList.toggle("active", i === currentSlide));
  [...navList.querySelectorAll("button")].forEach((button, i) => button.classList.toggle("active", i === currentSlide));

  updateUi();
  runSlideHook();
}

function updateUi() {
  slideCounter.textContent = `${currentSlide + 1} / ${slides.length}`;
  progressBar.style.width = `${((currentSlide + 1) / slides.length) * 100}%`;
  prevBtn.disabled = currentSlide === 0;
  nextBtn.disabled = currentSlide === slides.length - 1;
}

function runSlideHook() {
  const hook = slides[currentSlide].onShow;
  if (typeof hook === "function") hook();
}

function destroyChart(id) {
  if (charts[id]) {
    charts[id].destroy();
    delete charts[id];
  }
}

function commonChartOptions() {
  return {
    responsive: true,
    maintainAspectRatio: false,
    plugins: {
      legend: {
        position: "top",
        labels: {
          color: "#264343",
          font: { family: "Noto Sans Bengali", size: 11, weight: "700" }
        }
      },
      datalabels: {
        color: "#183f3b",
        font: { weight: "700", size: 10 }
      }
    }
  };
}

function renderCountsChart() {
  if (!window.Chart) return;
  const id = "countsChart";
  const ctx = document.getElementById(id);
  if (!ctx) return;
  destroyChart(id);

  charts[id] = new Chart(ctx, {
    type: "bar",
    data: {
      labels: ["সেন্টার", "সম্পদ", "সুপারিশ", "ঝুঁকি"],
      datasets: [
        {
          label: "সংখ্যা",
          data: [projectData.centers.length, projectData.assets.length, projectData.recommendations.length, projectData.risks.length],
          borderRadius: 8,
          backgroundColor: ["#0f766e", "#14b8a6", "#f59e0b", "#3b82f6"]
        }
      ]
    },
    options: {
      ...commonChartOptions(),
      scales: {
        y: { beginAtZero: true, ticks: { precision: 0 } }
      }
    }
  });
}

